#include<stdio.h>
int main()
{
	int i,j,n;
	scanf("%d",&n);
	for(i=0;i<=n;i++)
	{
		for(j=0;j<=n*2;j++)
		{
			printf("*");
		}
		printf("\n");
	}
	
	for(i=0;i<=n;i++)
	{
		for(j=1;j<=i;j++)
		{
			printf(" ");
		}
		for(j=n;j>=i;j--)
		{
			printf("+");
		}
		for(j=n;j>=i+1;j--)
		{
			printf("+");
		}
		printf("\n");
	}
	return 0;
}




